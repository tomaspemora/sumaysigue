% --- Executes just before configuration is made visible.
function configuration_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to configuration (see VARARGIN)

% Choose default command line output for configuration
handles.output = hObject;
warning off;
javaFrame = get(hObject,'JavaFrame');
javaFrame.setFigureIcon(javax.swing.ImageIcon('icon.jpg'));
warning on;


% Update handles structure
guidata(hObject, handles);

parametros  = xml2struct([pwd filesep 'datos.xml']);
fl_data     = parametros.data.fl_data.Text;         % FILA EN QUE EMPIEZA LA DATA DE PLANTILLA
r_col_rut   = parametros.data.r_col_rut.Text;       % INDICE COLUMNA DE RUT EN PLANTILLA
r_col_data_1  = parametros.data.r_col_data_1.Text;  % INDICE COLUMNA EN QUE TERMINAN LOS DATOS DE PERSONAS EN PLANTILLA
r_col_data_2  = parametros.data.r_col_data_2.Text;  % INDICE COLUMNA EN QUE EMPIEZAN LAS RESPUESTAS EN PLANTILLA
N           = parametros.data.N.Text;               % NUMERO DE ALUMNOS
string_flag = parametros.data.string_flag.Text;     % STRING PARA IDENTIFICAR LAS PREGUNTAS
a_col_rut   = parametros.data.a_col_rut.Text;       % INDICE COLUMNA DE RUT EN ARCHIVOS RESPUESTAS
fl_a_data   = parametros.data.fl_a_data.Text;       % FILA EN QUE EMPIEZAN LAS RESPUESTAS EN ARCHIVOS DE RESPUESTAS
pes_rev     = parametros.data.pes_rev.Text;         % NOMBRE PESTAÑA DE REVISION 
pes_abi     = parametros.data.pes_abi.Text;         % NOMBRE PESTAÑA DE PREGUNTAS ABIERTAS
handles.edit7.String = N;
handles.edit1.String = r_col_rut;
handles.edit2.String = r_col_data_1;
handles.edit3.String = fl_data;
handles.edit4.String = string_flag;
handles.edit5.String = a_col_rut;
handles.edit6.String = fl_a_data;
handles.edit8.String = r_col_data_2;
handles.edit9.String = pes_rev;
handles.edit10.String = pes_abi;

guidata(hObject, handles);

imshow('imagen_exp_1.png');
handles.pushbutton4.UserData.img = 1;
